#include <iostream>
#include <string>
using namespace std;

int main()
{
int result = 0;
int n = 0;
cin >> n;
char in[450];
for (int x = 0; x < n * 3; x++){
     cin >> in[x];
}
for(int x = 1; x < n*3; x += 3){
    if(in[x]=='+'){
        result ++;
    }
    if(in[x]=='-'){
        result --;
    }
}
cout << result << endl;
}